def parse_snap_ts(folder_name):
    """Parse a snapshot folder name into a timezone-aware datetime.

    Handles two formats:
      snap_2026-05-21_07-00-51         (from drive_archeology.py)
      _reliability_hrst_snapshot_laptop_20260522_0139  (frozen snapshots, no second granularity)
    """
    # Drive archeology pattern: snap_YYYY-MM-DD_HH-MM-SS
    m = re.match(r"snap_(\d{4})-(\d{2})-(\d{2})_(\d{2})-(\d{2})-(\d{2})", folder_name)
    if m:
        y, mo, d, h, mi, s = map(int, m.groups())
        return datetime(y, mo, d, h, mi, s, tzinfo=timezone.utc)
    # Frozen snapshot pattern: ..._YYYYMMDD_HHMM (no seconds)
    m = re.search(r"(\d{4})(\d{2})(\d{2})_(\d{2})(\d{2})$", folder_name)
    if m:
        y, mo, d, h, mi = map(int, m.groups())
        return datetime(y, mo, d, h, mi, 0, tzinfo=timezone.utc)
    return None


def build_snapshot_index():
    """Return {(filename, subdir): [(timestamp, full_path), ...]} sorted by timestamp."""
    index = {(f, sd): [] for f, sd in ARCHEOLOGY_FILES}

    # ---- Archeology root: data/_archeology/<filename>/snap_<ts>/<filename> ----
    for filename, subdir in ARCHEOLOGY_FILES:
        arch_file_root = ARCHEOLOGY_ROOT / filename
        if not arch_file_root.exists():
            continue
        for snap_folder in arch_file_root.iterdir():
            if not snap_folder.is_dir():
                continue
            ts = parse_snap_ts(snap_folder.name)
            if ts is None:
                continue
            csv_path = snap_folder / filename
            if csv_path.exists():
                index[(filename, subdir)].append((ts, csv_path))

    # ---- Frozen snapshot dirs (full data/ mirror at one moment) ----
    for frozen_dir in FROZEN_SNAPSHOT_DIRS:
        if not frozen_dir.exists():
            continue
        ts = parse_snap_ts(frozen_dir.name)
        if ts is None:
            continue
        for filename, subdir in ARCHEOLOGY_FILES:
            if subdir:
                candidate = frozen_dir / subdir / filename
            else:
                candidate = frozen_dir / filename
            if candidate.exists():
                index[(filename, subdir)].append((ts, candidate))

    # Sort each list by timestamp
    for key in index:
        index[key].sort(key=lambda x: x[0])

    return index


def find_closest_snapshot(index_entries, target_dt, max_delta_minutes=360):
    """Find the snapshot timestamp closest to target_dt, within tolerance.

    Preference order (AFTER beats BEFORE):
      1. Snapshot taken AFTER target_dt — truncation drops the > target_dt rows,
         so the staged file's last row ≈ target_dt. Inference at signal_dt is
         then perfectly aligned.
      2. Snapshot taken BEFORE target_dt — staged file's last row is whenever
         the snapshot's last data row is, which can be HOURS older than
         signal_dt. The model trains on stale data and predicts at the wrong
         timestamp.

    Within each tier, pick the snapshot closest in time.

    Returns (chosen_ts, chosen_path) or None if nothing within tolerance.
    """
    if not index_entries:
        return None
    tolerance = timedelta(minutes=max_delta_minutes)

    # Tier 1: AFTER target (truncation gives data up to exactly target_dt)
    after = [(ts, p) for ts, p in index_entries
             if ts >= target_dt and ts - target_dt <= tolerance]
    if after:
        return min(after, key=lambda x: x[0] - target_dt)

    # Tier 2: BEFORE target (staged data is stale by however long ago snapshot was)
    before = [(ts, p) for ts, p in index_entries
              if ts < target_dt and target_dt - ts <= tolerance]
    if before:
        return max(before, key=lambda x: x[0])  # most recent before
    return None


def build_config_snapshot_index(filename):
    """Index of archeology snapshots of a single config-style file (prod CSV
    or regime config JSON).

    Returns sorted list of (timestamp, path) tuples.
    """
    out = []
    arch_file_root = ARCHEOLOGY_ROOT / filename
    if arch_file_root.exists():
        for snap_folder in arch_file_root.iterdir():
            if not snap_folder.is_dir():
                continue
            ts = parse_snap_ts(snap_folder.name)
            if ts is None:
                continue
            f = snap_folder / filename
            if f.exists():
                out.append((ts, f))
    out.sort(key=lambda x: x[0])
    return out


def build_merged_archeology(index_entries, filename):
    """Stack all snapshots oldest-first; for each row keep value from the
    OLDEST snapshot that contained it.

    Reverses the data-drift damage caused by keep='last' in download_macro_data:
    every snapshot's older rows have been overwritten by repeated re-fetches,
    so newer snapshots carry MORE drift than older ones. The oldest snapshot
    that contains a given row has the value closest to "originally observed".

    The merge keeps the ORIGINAL string format of the time column so the
    engine's pd.read_csv reads it identically to the current data/ file
    (no tz-awareness drift between merged and original).

    Returns (DataFrame, {snap_iso: rows_contributed}) or None if the file
    lacks a usable time column.
    """
    if not index_entries:
        return None

    sample_path = index_entries[0][1]
    sample_df = pd.read_csv(sample_path, nrows=5)
    key_col = None
    for cand in ("datetime", "date", "timestamp"):
        if cand in sample_df.columns:
            key_col = cand
            break
    if key_col is None:
        return None

    merged = None
    seen_keys = set()  # parsed-datetime set for deduplication
    snap_origin_count = {}
    for ts, path in index_entries:  # already sorted oldest -> newest
        try:
            df = pd.read_csv(path)
        except Exception:
            continue
        if key_col not in df.columns:
            continue
        # Parse to compare keys, but keep ORIGINAL string column intact for output.
        parsed = pd.to_datetime(df[key_col], errors="coerce", utc=True)
        df = df[parsed.notna()].copy()
        parsed = parsed[parsed.notna()]
        new_mask = ~parsed.isin(seen_keys)
        new_rows = df[new_mask.values]
        if len(new_rows):
            if merged is None:
                merged = new_rows.copy()
            else:
                merged = pd.concat([merged, new_rows], ignore_index=True)
            seen_keys.update(parsed[new_mask.values].tolist())
            snap_origin_count[ts.isoformat()] = len(new_rows)

    if merged is None:
        return None
    # Sort by parsed datetime (without mutating the original string column).
    sort_keys = pd.to_datetime(merged[key_col], errors="coerce", utc=True)
    merged = merged.iloc[sort_keys.argsort().values].reset_index(drop=True)
    return merged, snap_origin_count


def pick_config_snapshot_at(index_entries, target_dt):
    """Pick the prod-CSV / regime-config snapshot LIVE at target_dt.

    Prefers the most recent snapshot taken AT OR BEFORE target_dt — that's the
    file the trader's hot-reload would have been reading. Falls back to the
    earliest available if no eligible (≤target) snapshot exists.

    Returns (chosen_ts, chosen_path) or None if index is empty.
    """
    if not index_entries:
        return None
    eligible_before = [(ts, p) for ts, p in index_entries if ts <= target_dt]
    if eligible_before:
        return max(eligible_before, key=lambda x: x[0])
    return index_entries[0]  # earliest if nothing strictly before


