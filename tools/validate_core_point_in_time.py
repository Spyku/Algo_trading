# Stage snapshots into working dir for the engine to read
# ============================================================================
def _truncate_and_write(source, dest, signal_dt):
    """Read CSV from source, drop rows with datetime/date > signal_dt, write to dest.

    The point-in-time guarantee: the engine must only ever see rows that
    existed AT OR BEFORE signal_dt. Without truncation, the snapshot's last
    row could be hours past signal_dt, and the engine would train + predict
    on that future row — turning a PIT validation into a peeking validation.

    Returns (n_rows_kept, last_kept_timestamp_iso or None).
    """
    df = pd.read_csv(source)
    if df.empty:
        df.to_csv(dest, index=False)
        return (0, None)

    # Pick the time column. Macro/derivatives use 'date'; price uses 'datetime'.
    time_col = None
    for cand in ("datetime", "date", "timestamp"):
        if cand in df.columns:
            time_col = cand
            break

    if time_col is None:
        # No time column — can't truncate. Copy as-is.
        df.to_csv(dest, index=False)
        return (len(df), None)

    parsed = pd.to_datetime(df[time_col], errors="coerce", utc=True)
    # Drop rows with unparseable timestamps to be safe.
    keep_mask = parsed.notna() & (parsed <= signal_dt)
    df_trunc = df.loc[keep_mask].copy()
    df_trunc.to_csv(dest, index=False)
    last_ts = parsed[keep_mask].max() if keep_mask.any() else None
    return (len(df_trunc), last_ts.isoformat() if last_ts is not None else None)


def stage_snapshots(snapshots, signal_dt, log=None):
    """Copy chosen snapshot files into the working dir, truncated to <= signal_dt.

    Also deletes any staged file from a PRIOR iteration that's NOT in this
    iteration's snapshots list — without this, a file staged for sample N
    (because archeology was within tolerance then) would leak into sample N+1
    (whose archeology might be out-of-tolerance, expected to fall back to
    current data/). The redirect monkey-patch only falls back when the staged
    file doesn't exist, so we must remove stale stages explicitly.

    snapshots: list of (filename, subdir, source_path) tuples.
    signal_dt: timezone-aware datetime; all rows with time > signal_dt are dropped.
    Returns {filename: (n_rows, last_ts_iso)} for diagnostics.
    """
    (WORKING_DATA_DIR / "macro_data").mkdir(parents=True, exist_ok=True)
    stats = {}

    # First: clean stale stages — any file in ARCHEOLOGY_FILES that isn't being
    # restaged this iteration must be removed, so the engine falls back to live.
    staged_dest_paths = set()
    for filename, subdir, _ in snapshots:
        dest = (WORKING_DATA_DIR / subdir / filename) if subdir else (WORKING_DATA_DIR / filename)
        staged_dest_paths.add(dest)
    for filename, subdir in ARCHEOLOGY_FILES:
        dest = (WORKING_DATA_DIR / subdir / filename) if subdir else (WORKING_DATA_DIR / filename)
        if dest not in staged_dest_paths and dest.exists():
            try:
                dest.unlink()
            except PermissionError:
                time.sleep(0.5)
                try:
                    dest.unlink()
                except PermissionError:
                    pass  # leak rather than crash; the result is just one stale sample

    for filename, subdir, source in snapshots:
        if subdir:
            dest = WORKING_DATA_DIR / subdir / filename
        else:
            dest = WORKING_DATA_DIR / filename
        dest.parent.mkdir(parents=True, exist_ok=True)
        # Try a few times — Drive sync occasionally holds a short-lived lock
        for attempt in range(3):
            try:
                if dest.exists():
                    dest.unlink()
                n_rows, last_ts = _truncate_and_write(source, dest, signal_dt)
                stats[filename] = (n_rows, last_ts)
                break
            except PermissionError:
                if attempt == 2:
                    raise
                time.sleep(0.5)
    return stats


