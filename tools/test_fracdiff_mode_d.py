"""test_fracdiff_mode_d.py — Smoke test for Idea #1 (fractional differentiation features).

What this does:
  1. Monkey-patches `build_hourly_features` to add 5 fractional-diff features
     (different d values on log-price + log-volume) WITHOUT modifying the engine
     source file.
  2. Runs Mode D (grid search) on ETH 5,6,7,8h with --replay 1440 in PARALLEL
     across the 4 horizons via subprocess.ProcessPoolExecutor.
     - --no-persist: writes to crypto_ed_production_noprod.csv
     - --no-data-update: skip macro+OHLCV redownload (must run AFTER engine
       has already updated data once today, OR you don't care about the freshest hour)
  3. After all 4 finish, audits which horizons selected fracdiff features in
     their grid winners + LGBM importance ranking.

The monkey-patch is in-process per subprocess — uses sitecustomize.py-style
trick: each subprocess imports a tiny patcher module FIRST, which patches the
engine, then runs the engine.

Output:
  - models/crypto_ed_grid_ETH_<h>h_FRACDIFF.csv  (grid results, tagged)
  - logs/fracdiff_smoke_<h>h_<ts>.log            (per-horizon log)
  - logs/fracdiff_smoke_summary_<ts>.txt         (cross-horizon audit)

Usage on Desktop:
  python tools/test_fracdiff_mode_d.py
"""
from __future__ import annotations

